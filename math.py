# Add Implementation
def add(x,y):
	return x + y

# Subtract implementation
def subtract(x,y):
	return x-y

# Multiply implementation
def multiply(x,y):
	return x*y

# Divide Implementation
def divide(x,y):
	if y== 0:
		return "Error: Cannot divide by 0"
	else:
		return x/y

